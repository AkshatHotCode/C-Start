#include <stdio.h>

// Arithmetic operators
int main()
{
    int a, b;

    a = 10;
    b = 2;

    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
}

